package com.bbs.ControlStatement;

public class ifelseif {
	public static void main(String[] args) {
		int x = 2;
		if (x > 10) {
			System.out.println("i is greater than 10");
		} else if (x < 10) {
			System.out.println("i  is less than 10");
		} else {
			System.out.println("i is 10");
		}
	}

}
